package moe.osudroid.assets.ui;

public final class UiAssetEntry {
    private UiAssetKey key;
    private String path;
    private UiAssetOrigin origin;

    public UiAssetKey getKey() {
        return key;
    }

    public String getPath() {
        return path;
    }

    public UiAssetOrigin getOrigin() {
        return origin;
    }
}
